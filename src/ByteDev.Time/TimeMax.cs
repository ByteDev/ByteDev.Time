namespace ByteDev.Time
{
    /// <summary>
    /// Represents maximum time constants in a date time.
    /// </summary>
    public static class TimeMax
    {
        /// <summary>
        /// Max number of possible hours in a date time.
        /// </summary>
        public const int Hours = 23;

        /// <summary>
        /// Max number of possible minutes in a date time.
        /// </summary>
        public const int Minutes = 59;

        /// <summary>
        /// Max number of possible seconds in a date time.
        /// </summary>
        public const int Seconds = 59;

        /// <summary>
        /// Max number of possible milliseconds in a date time.
        /// </summary>
        public const int Milliseconds = 999;
    }
}